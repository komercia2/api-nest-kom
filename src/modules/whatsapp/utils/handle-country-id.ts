export const handleCountryID = (countryID: number) => {
	if (countryID === 1) return "Colombia 🇨🇴"
	if (countryID === 2) return "Internacional 🌎"
	if (countryID === 3) return "México 🇲🇽"
	if (countryID === 6) return "Argentina 🇦🇷"
	if (countryID === 7) return "Chile 🇨🇱"
	if (countryID === 8) return "Puerto Rico 🇵🇷"
	if (countryID === 9) return "Perú 🇵🇪"
	if (countryID === 10) return "Panama 🇵🇦"
}
